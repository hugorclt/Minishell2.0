#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	long long	result;
	int			signe;

	i = 0;
	result = 0;
	signe = 1;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		if (result * signe > 2147483647)
			return (-1);
		if (result * signe < -2147483648)
			return (0);
		i++;
	}
	return (result * signe);
}
